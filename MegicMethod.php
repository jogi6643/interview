The function names
__construct(), __destruct(), __call(), __callStatic(), __get(), __set(), __isset(), __unset(), 
__sleep(), __wakeup(), __serialize(), __unserialize(), __toString(), __invoke(), __set_state(),
 __clone() and __debugInfo()
are magical in PHP classes.
 You cannot have functions with these names in any of your classes unless 
 you want the magic functionality associated with them.